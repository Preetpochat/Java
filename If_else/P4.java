class Demo{
	public static void main(String [] args){
		int x = 7;
	
		if(x == 0){
		System.out.println("Zero");
		}
		else if(x == 1){
                System.out.println("ONE");
                }else if(x == 2){
                System.out.println("Two");
                }else if(x == 3){
                System.out.println("Three");
                }else if(x == 4){
                System.out.println("Four");
                }
		else if(x == 5){
                System.out.println("Five");
                }
		else{
                System.out.println("Invalid");
                }
	
	
	}
}
